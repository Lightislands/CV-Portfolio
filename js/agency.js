//Scroll-page-up
 var top_show = 400; // In what position of scroll bars to start showing the "Top"
  var delay = 300; // Scroll delay
  $(document).ready(function() {
    $(window).scroll(function () {
      if ($(this).scrollTop() > top_show) $('#top_scr').fadeIn();
      else $('#top_scr').fadeOut();
    });
    $('#top_scr').click(function () {
      $('body, html').animate({
        scrollTop: 0
      }, delay);
    });
  });
